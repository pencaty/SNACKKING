package com.example.snackking;

public class Snack_DataStructure {

    private String snack_name;
    private String snack_taste;
    private String snack_cost;
    private String snack_number_of_rate;

    public String getSnack_name() {
        return snack_name;
    }
    public String getSnack_taste() {
        return snack_taste;
    }
    public String getSnack_cost() {
        return snack_cost;
    }
    public String getSnack_number_of_rate() {
        return snack_number_of_rate;
    }

    public void setSnack_name(String snack_name) {
        this.snack_name = snack_name;
    }
    public void setSnack_taste(String snack_taste) {
        this.snack_taste = snack_taste;
    }
    public void setSnack_cost(String snack_cost) {
        this.snack_cost = snack_cost;
    }
    public void setSnack_number_of_rate(String snack_number_of_rate) {
        this.snack_number_of_rate = snack_number_of_rate;
    }
}